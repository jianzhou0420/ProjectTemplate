# There are 6 folders in this project template, they are divided as two groups: supporting and main codes



# Supporting folders:
## "data/" folder is used to store all datasets and outputs
## "scripts/" folder is used to store all bash scripts for training and testing or other purposes
## "third_party/" folder is used to store all third party codes and libraries
## "zmisc/" folder is used to store miscellaneous files



# Main code folders:
## "projectName" folder is used to store all main codes for the project
## "workspace/" folder is used to store all training and testing related main codes

